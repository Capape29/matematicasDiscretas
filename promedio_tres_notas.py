# promedio de tres notas

# input
a = float(input("Ingrese la primera nota del estudiante: "))
b = float(input("Ingrese la segunda nota del estudiante: "))
c = float(input("Ingrese la tercera nota del estudiante: "))

# processing
promedio = (a + b + c)/3

# output
print("El promedio de las tres notas ingresadas es de: ", promedio)